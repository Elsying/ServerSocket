package serversocket;

public class Message {
	private String ToID;
	private String Msg;
	private String MyID;
	public String getToID() {
		return ToID;
	}
	public void setToID(String toID) {
		ToID = toID;
	}
	public String getMsg() {
		return Msg;
	}
	public void setMsg(String msg) {
		Msg = msg;
	}
	public String getMyID() {
		return MyID;
	}
	public void setMyID(String myID) {
		MyID = myID;
	}

	

}
